drop table MULTE;
drop table CONSEGNA;
drop table AZIENDA;
drop table FATTORINO;

CREATE TABLE FATTORINO
(FID  SMALLINT NOT NULL,
 NOME   CHAR(15) NOT NULL,
 INIZIALI  CHAR(3),
 ANNO_NASCITA SMALLINT,
 SESSO   CHAR(1),
 ANNO_ASSUNZIONE  SMALLINT NOT NULL,
 VIA  CHAR(15) NOT NULL,
 NUMEROVIA  CHAR(4),
 CAP  CHAR(6),
 CITTA   CHAR(10) NOT NULL,
 CELL  CHAR(10),
 TEL CHAR(4),
 PRIMARY KEY(FID)
 );

CREATE TABLE AZIENDA
 (AID  SMALLINT NOT NULL,
 FID  SMALLINT NOT NULL,
 INCARICO  CHAR(20) NOT NULL,
 PRIMARY KEY(AID),
 FOREIGN KEY(FID) REFERENCES FATTORINO(FID)
 );

CREATE TABLE CONSEGNA
 (AID  SMALLINT NOT NULL ,
 FID  SMALLINT NOT NULL,
 NUMCONSEGNE   SMALLINT NOT NULL,
 NUMRITIRI   SMALLINT NOT NULL,
 PRIMARY KEY (AID,FID),
 FOREIGN KEY(FID) REFERENCES FATTORINO(FID),
 FOREIGN KEY(AID) REFERENCES AZIENDA(AID)
 );

CREATE TABLE MULTE
 (MID INTEGER NOT NULL,
 FID  SMALLINT NOT NULL,
 DATA   DATE NOT NULL,
 COSTO  DECIMAL(7,2)   NOT NULL,
 PRIMARY KEY(MID),
 FOREIGN KEY(FID) REFERENCES FATTORINO(FID)
  );

