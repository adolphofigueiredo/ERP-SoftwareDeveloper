import random  # Importing the random module to use random functions

# Generating random numbers
num = random.random()  # Generates a random floating-point number between 0 and 1
num2 = random.randint(1, 10)  # Generates a random integer between 1 and 10 (inclusive)

# Output section
print('Result:', num, num2)  # Prints the generated random numbers
