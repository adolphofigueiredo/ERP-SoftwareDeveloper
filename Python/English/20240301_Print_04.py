#\033[m] - Return to the default configuration.

# Text style
#0 - Do nothing.
#1 - Bold text.
#4 - Underlined text.
#7 - Inverts the background and text color settings.

# Text color
#30 - White
#31 - Red
#32 - Green
#33 - Yellow
#34 - Blue
#35 - Purple
#36 - Light Blue
#37 - Gray (Default color)

# Background colors
#40 - White
#41 - Red
#42 - Green
#43 - Yellow
#44 - Blue
#45 - Purple
#46 - Light Blue
#47 - Gray (Default color)
