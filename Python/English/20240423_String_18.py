n1 = input('Enter a value: ')
print(n1.isalpha())  # Checks if all characters in the input are alphabetic
print(n1.isnumeric())  # Checks if all characters in the input are numeric
print(n1.isalnum())  # Checks if all characters in the input are alphanumeric (alphabetic or numeric)
print(n1.islower())  # Checks if all characters in the input are lowercase
print(n1.isupper())  # Checks if all characters in the input are uppercase
print(n1.isspace())  # Checks if all characters in the input are whitespace
